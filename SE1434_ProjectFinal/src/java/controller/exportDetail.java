        int total = totalFinish + totalLow+ totalQuali;
        request.setAttribute("export", export);
        request.setAttribute("totalFinish", totalFinish);
        request.setAttribute("totalLow", totalLow);
        request.setAttribute("totalQuali", totalQuali);
        request.setAttribute("total", total);
        request.getRequestDispatcher("jsp/exportDetail.jsp").forward(request, response);
